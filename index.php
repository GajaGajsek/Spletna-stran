            function sestava_izjave(element){
                
                let figure = document.createElement("figure");
                figure.className = "izjava";

                let blockquote = document.createElement("blockquote");
                figure.appendChild(blockquote);

                let besedilo = document.createElement("p");
                besedilo.className = "izjava-opis";
                besedilo.textContent = element.besedilo;
                blockquote.appendChild(besedilo);

                              
                let slika = document.createElement("img");
                figure.appendChild(slika);
                slika.src = element.slika;
                slika.alt = element.ime_stranke;

                let figcaption = document.createElement("figcaption");
                figure.appendChild(figcaption);
                
                let cite = document.createElement("cite");
                cite.className = "izjava-glava";
                cite.textContent = element.ime_stranke;
                figcaption.appendChild(cite);

                let cas = document.createElement("time");
                cas.className = "izjava-noga";
                cas.dateTime = element.datum;
                cas.textContent = element.datum;
                figcaption.appendChild(cas);

                return figure;
            }

